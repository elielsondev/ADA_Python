# Funções para Listas
lista = [2, 5, 8, 11, 14, 19, 22]

# len()
print('A lista possui quantos elementos?', len(lista))
# A lista possui quantos elementos? 7

# sum()
print('A soma dos valores da lista é:', sum(lista))  # 81
# A soma dos valores da lista é: 81

# max()
print('Maior elemento da lista:', max(lista))
# Maior elemento da lista: 22

# mim()
print('Menor elemento da lista:', min(lista))
# Menor elemento da lista: 2
