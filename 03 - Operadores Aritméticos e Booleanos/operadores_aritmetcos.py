# > Operações Matemáticas (Aritméticas)

'''
  - Soma: +
  - Subtração: -
  - Multiplicação: *
  - Divisão: /
  - Divisão inteira: //
  - Resto ou Módulo: %
  - Potência: **
'''

num1 = 10
num2 = 2


print(num1 + num2)  # Saída: 12
print(num1 - num2)  # Saída: 8
print(num1 * num2)  # Saída: 20
print(num1 / num2)  # Saída: 5.0
print(num1 // num2)  # Saída: 5
print(num1 % num2)  # Saída: 0
print(num1 ** num2)  # Saída: 100
